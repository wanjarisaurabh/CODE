#include <bits/stdc++.h>
using namespace std;

vector<int>twoSum(int n, vector<int> &arr, int target) {
    sort(arr.begin(), arr.end());
    int left = 0, right = n - 1;
    while (left < right) {
        int sum = arr[left] + arr[right];
        if (sum == target) {
            return {left, right};
        }
        else if (sum < target) left++;
        else right--;
    }
    return {-1, -1};
}

